const transform = (lang: Language): Language => {
  const transformResult = lang;

  (Object.keys(en) as Array<keyof Language>).forEach((key: keyof Language) => {
    if (transformResult[key] === undefined) {
      transformResult[key] = `(NT) ${en[key]}`;
    }
  });

  return transformResult;
};

